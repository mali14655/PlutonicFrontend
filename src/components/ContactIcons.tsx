type ContactRowProps = {
  icon: ReactNode;
  children: ReactNode;
  href?: string;
  variant?: 'footer' | 'card';
};

export function ContactInfoRow({ icon, children, href, variant = 'card' }: ContactRowProps) {
  const iconBox =
    variant === 'footer'
      ? 'w-9 h-9 rounded-lg bg-white/12 border border-white/10 text-plutonic-gold flex items-center justify-center shrink-0'
      : 'w-10 h-10 rounded-xl bg-sky-100 text-sky-600 flex items-center justify-center shrink-0';

  const content = (
    <div className="flex items-start gap-3">
      <div className={iconBox}>{icon}</div>
      <div className="min-w-0 pt-0.5">{children}</div>
    </div>
  );

  if (href) {
    return (
      <a
        href={href}
        className={`block transition ${variant === 'footer' ? 'hover:opacity-90 group' : 'hover:opacity-80'}`}
        {...(href.startsWith('http') || href.startsWith('tel') || href.startsWith('mailto')
          ? { target: href.startsWith('http') ? '_blank' : undefined, rel: href.startsWith('http') ? 'noreferrer' : undefined }
          : {})}
      >
        {content}
      </a>
    );
  }

  return content;
}
